def strip_lead_dash(s: str) -> str:
    s = s.strip()
    while True:
        if s.startswith("- "):
            s = s[2:].strip()
        elif s.startswith("\u2013 "):
            s = s[2:].strip()
        else:
            break
    return s


def dialogue_tail(line: str) -> str:
    line = line.strip()
    if line.startswith("**") and ":**" in line:
        return line.split(":**", 1)[1].strip()
    return strip_lead_dash(line)


def table_two_cells(line: str) -> tuple[str, str]:
    parts = [p.strip() for p in line.strip().split("|")[1:-1]]
    if len(parts) >= 2:
        return parts[0], parts[1]
    return parts[0] if parts else "", ""


def strip_book_italics(s: str) -> str:
    s = re.sub(r"\*\([^)]*\)\*", "", s)
    s = re.sub(r"\s+,", ",", s)
    return re.sub(r"\s+", " ", s).strip()


def panel_caption(line: str) -> str:
    """Строка подписи к картинке (убрать нумерацию и сноски)."""
    s = strip_book_italics(line)
    s = re.sub(r"^\d+\.\s*", "", s).strip()
    return s


