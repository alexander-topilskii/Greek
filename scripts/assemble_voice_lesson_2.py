"""Assemble book/pages/lesson_2/lesson_voice_2/voice_lesson_2.md from lesson_2_digitized.md slices."""

from __future__ import annotations

from pathlib import Path


def main() -> None:
    repo = Path(__file__).resolve().parents[1]
    digit_path = repo / "book/pages/lesson_2/lesson_digitized/lesson_2_digitized.md"
    out_path = repo / "book/pages/lesson_2/lesson_voice_2/voice_lesson_2.md"
    out_path.parent.mkdir(parents=True, exist_ok=True)

    lines = digit_path.read_text(encoding="utf-8").splitlines()
    ANCHOR_LINE_BASE = 29
    _cur_anchor = next(i for i, ln in enumerate(lines) if '<a id="' in ln)
    _slice_shift = ANCHOR_LINE_BASE - _cur_anchor

    def sl(a: int, b: int) -> str:
        return "\n".join(lines[a - _slice_shift : b - _slice_shift]).rstrip()

    mic = chr(0x1F399)
    house = chr(0x1F3E0)
    book = chr(0x1F4DA)
    page = chr(0x1F4C4)
    zap = chr(0x26A1)
    gem = chr(0x1F48E)

    a13 = sl(74, 82)
    informal_formal_table = sl(144, 151)
    marina_example = sl(154, 156)
    greetings = sl(161, 175)
    mood = sl(178, 188)
    addr_demo = sl(135, 141)
    kallithea_demo = sl(539, 546)
    a16 = sl(622, 626)
    phone_cues = sl(633, 639)
    live_where_cues = sl(96, 102)
    meno_paradigms = sl(270, 280)
    article_help = sl(349, 361)
    city_labels = sl(333, 347)

    out: list[str] = []

    def ln(s: str = "") -> None:
        out.append(s)

    ln(f"# {mic} Голосовой урок 2 (по оцифровке главы)")
    ln()
    ln("### Блок 1: Коммуникативные ситуации и ролевые игры")
    ln()

    ln("#### Ситуация 1 — У периптера: где живёте")
    ln()
    ln("*Контекст:* Улица у **περίπτερο**, диалог **A13** в оцифровке. Цель — цепочка «приветствие → дела → где живёшь → адрес».")
    ln()
    ln("```text")
    ln(a13)
    ln("```")
    ln()
    ln("*Роль А (линия Φου):* Отработайте все реплики Φου из цитаты: приветствие, ответы на вопросы о делах, ответ «мне отлично», фраза с μένω и районом, уточнение адреса, встречный вопрос «а ты где живёшь?» — строго по тексту.")
    ln()
    ln("*Роль Б (линия Παναγιώτης):* Отработайте реплики Παναγιώτης из цитаты: вечернее приветствие, вопрос «живёшь рядом?», «где именно?», ответ с Пиреем и Пасалимани.")
    ln()
    ln("*Чек-фразы:* опирайтесь на диалог выше; ключ — пара вопросов про «рядом» и «где именно», плюс обмен районом и улицей.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 2 — Неформально и официально")
    ln()
    ln("*Контекст:* Таблица **ανεπίσημο / επίσημο** и второй пример диалога.")
    ln()
    ln("```text")
    ln(informal_formal_table)
    ln()
    ln(marina_example)
    ln("```")
    ln()
    ln("*Роль А:* Сначала говорите форму **Τι κάνεις;** / **Πώς είσαι;**, затем переключитесь на **Τι κάνετε;** / **Πώς είστε;**.")
    ln()
    ln("*Роль Б:* Ответьте по цепочке второго примера в цитате (утреннее приветствие и взаимные Τι κάνεις / Τι κάνει ο …).")
    ln()
    ln("*Чек-фразы:* «Τι κάνεις;», «Τι κάνετε;», «Πώς πάει;», «Τι γίνεται;», «Τι νέα;», «Τι κάνει ο Παναγιώτης;».")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 3 — Настроение и прощание")
    ln()
    ln("*Контекст:* Жёлтый блок **χαιρετισμοί και αποχαιρετισμοί** и таблица **διάθεση**.")
    ln()
    ln("```text")
    ln(greetings)
    ln()
    ln(mood)
    ln("```")
    ln()
    ln("*Роль А:* Спросите «Τι κάνεις;» или «Πώς είσαι;» → получите ответ из таблицы настроения → завершите прощанием из списка.")
    ln()
    ln("*Роль Б:* Ответьте одной строкой из таблицы настроения в цитате → попрощайтесь фразой из нижнего списка в цитате.")
    ln()
    ln("*Чек-фразы:* см. два списка в цитате.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 4 — Район, улица, уточнение")
    ln()
    ln("*Контекст:* Сначала короткий пример с Καλαμαριά и Πόντου 16, затем образец 1.2 (Каллифея / κέντρο / Μενάνδρου 10) из оцифровки.")
    ln()
    ln("```text")
    ln(addr_demo)
    ln()
    ln(kallithea_demo)
    ln("```")
    ln()
    ln("*Роль А:* Задайте цепочку из образца 1.2: где живёшь → «а ты?» → где именно.")
    ln()
    ln("*Роль Б:* Ответьте по строкам образца: район → κέντρο → улица и номер (как Μενάνδρου 10).")
    ln()
    ln("*Чек-фразы:* повторите формулировки из второй цитаты; не выходите за пределы оцифровки.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 5 — Телефон")
    ln()
    ln("*Контекст:* **A16** и зелёный блок «Πώς το λένε;» про телефон.")
    ln()
    ln("```text")
    ln(a16)
    ln()
    ln(phone_cues)
    ln("```")
    ln()
    ln("*Роль А:* Реплики Φου из первой цитаты: есть ли номер → продиктуйте номер → встречный вопрос про телефон.")
    ln()
    ln("*Роль Б:* Реплики Παναγιώτης: короткий отрицательный ответ → ответ с κινητό и σταθερό, как в тексте.")
    ln()
    ln("*Чек-фразы:* используйте вторую цитату (типовые вопросы и отрицание «δεν έχω»).")
    ln()

    ln("### Блок 2: Шпаргалка для диалогов (фразы-клише)")
    ln()
    ln("*Ниже — готовые опоры из раздела «Πώς το λένε;» (жильё) и телефонного блока оцифровки.*")
    ln()
    ln("```text")
    ln(live_where_cues)
    ln()
    ln(phone_cues)
    ln("```")
    ln()
    ln("*Дополнительно:* в оцифровке есть раздел **1.1** с вопросами «Πού είναι …;» — используйте те же формулировки, если нужен географический круг.")
    ln()

    ln("### Блок 3: Тематический словарь (таблицы)")
    ln()
    ln("*Словарные группы ниже приведены как срезы оцифровки: подписи к иллюстрации, окончания по родам, примеры **μένω**, упражнение на **ο, η, το**.*")
    ln()
    ln("#### Подписи к иллюстрации «Για δες» (город)")
    ln()
    ln("```text")
    ln(city_labels)
    ln("```")
    ln()
    ln("#### Примеры с **μένω**")
    ln()
    ln("```text")
    ln(meno_paradigms)
    ln("```")
    ln()
    ln("#### Окончания и род (фрагмент таблицы)")
    ln()
    ln("```text")
    ln(article_help)
    ln("```")
    ln()
    ln("#### Упражнение «ο, η, το» (список слов)")
    ln()
    ln("```text")
    ln(sl(372, 400))
    ln("```")

    out_path.write_text("\n".join(out) + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
