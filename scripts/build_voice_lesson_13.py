def strip_lead_dash(s: str) -> str:
    s = s.strip()
    while True:
        if s.startswith("- "):
            s = s[2:].strip()
        elif s.startswith("\u2013 "):
            s = s[2:].strip()
        else:
            break
    return s


def dialogue_tail(line: str) -> str:
    line = line.strip()
    if line.startswith("**") and ":**" in line:
        return line.split(":**", 1)[1].strip()
    return strip_lead_dash(line)


def table_two_cells(line: str) -> tuple[str, str]:
    parts = [p.strip() for p in line.strip().split("|")[1:-1]]
    if len(parts) >= 2:
        return parts[0], parts[1]
    return (parts[0] if parts else ""), ""


def strip_colored_pos_bullet(s: str) -> str:
    """Строка вида `- **Πράσινο:** φράσεις` → только фразы (после `:**`)."""
    s = s.strip()
    if ":**" in s:
        return s.split(":**", 1)[1].strip()
    return strip_lead_dash(s)


