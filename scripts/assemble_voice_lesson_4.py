    lines = digit_path.read_text(encoding="utf-8").splitlines()
    ANCHOR_LINE_BASE = 26
    _cur_anchor = next(i for i, ln in enumerate(lines) if '<a id="' in ln)
    _slice_shift = ANCHOR_LINE_BASE - _cur_anchor

    def sl(a: int, b: int) -> str:
        return "\n".join(lines[a - _slice_shift : b - _slice_shift]).rstrip()

    mic = chr(0x1F399)
    house = chr(0x1F3E0)
    book = chr(0x1F4DA)
    page = chr(0x1F4C4)
    zap = chr(0x26A1)
    gem = chr(0x1F48E)

    a27 = sl(74, 92)
    pos_lene1 = sl(93, 112)
    mia_des_milao = sl(112, 127)
    mini_dialogues = sl(212, 227)
    a28 = sl(150, 159)
    pos_lene2 = sl(160, 170)
    nationality_lang = sl(180, 203)
    paradedigma_level = sl(228, 234)
    scale = sl(235, 245)
    apantao3 = sl(246, 255)
    sveta = sl(256, 263)
    dialog8 = sl(302, 315)
    profiles = sl(315, 330)
    accusative = sl(418, 452)

    out: list[str] = []

    def ln(s: str = "") -> None:
        out.append(s)

    ln(f"# {mic} Голосовой урок 4 (по оцифровке главы)")
