    ln()
    ln(
        "*Контекст:* Диалог **A27** и блок **Πώς το λένε;** сразу после него "
        "(списки фраз и синее σημείωμα — всё в цитате)."
    )
    ln()
    ln("```text")
    ln(a27)
    ln()
    ln(pos_lene1)
    ln("```")
    ln()
    ln(
        "*Роль А (первая линия в A27):* Дорога к парку, уточнение языков, кинотеатр — "
        "только реплики из цитаты."
    )
    ln()
    ln(
        "*Роль Б (вторая и третья линии):* Ответы и перевод на говорящего по-гречески — "
        "по репликам из цитаты."
    )
    ln()
    ln("*Чек-фразы:* списки и синий блок во второй части цитаты; артикли в вопросах — как в оцифровке.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 2 — Спряжение «говорю» и короткие обмены")
    ln()
    ln("*Контекст:* Таблица **Για δες** со спряжением и цветные мини-диалоги про языки.")
    ln()
    ln("```text")
    ln(mia_des_milao)
    ln()
    ln(mini_dialogues)
    ln("```")
    ln()
    ln("*Роль А:* Вопросы «ты говоришь на …?» по жёлтому и зелёному блокам — дословно из цитаты.")
    ln()
    ln("*Роль Б:* Короткие ответы и отрицания — только формы из цитаты.")
    ln()
    ln("*Чек-фразы:* строки таблицы спряжения и обе цветные цепочки.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 3 — После занятий: происхождение и языки")
    ln()
    ln("*Контекст:* Диалог **A28**, три фразы **Πώς το λένε;** и таблицы национальностей и языков.")
    ln()
    ln("```text")
    ln(a28)
    ln()
    ln(pos_lene2)
    ln()
    ln(nationality_lang)
    ln("```")
    ln()
    ln("*Роль А (первая линия в A28):* Комплимент за греческий, вопрос об происхождении — по тексту.")
    ln()
    ln("*Роль Б (остальные реплики):* Представление и комментарий о языках третьего лица — по цитате.")
    ln()
    ln("*Чек-фразы:* три нумерованные фразы во второй части и обе зелёные таблицы.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 4 — Насколько хорошо: пример, шкала, микрофон")
