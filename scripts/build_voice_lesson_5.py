def strip_lead_dash(s: str) -> str:
    s = s.strip()
    while True:
        if s.startswith("- "):
            s = s[2:].strip()
        elif s.startswith("\u2013 "):
            s = s[2:].strip()
        else:
            break
    return s


def dialogue_tail(line: str) -> str:
    line = line.strip()
    if line.startswith("**") and ":**" in line:
        return line.split(":**", 1)[1].strip()
    return strip_lead_dash(line)


def ex_greek(line: str) -> str:
    s = line.strip()
    s = re.sub(r"^-\s*\*[^*]+\*\s*", "", s)
    return s.strip()


def parse_country_rows(L: list[str]) -> list[str]:
    out: list[str] = []
    for i in range(368, 382):
        ln = L[i].strip()
        if not ln.startswith("|") or "|--" in ln:
            continue
        if "\u03a7\u03ce\u03c1\u03b1" in ln and "\u0386\u03bd\u03c4\u03c1\u03b1\u03c2" in ln:
            continue
        parts = [p.strip() for p in ln.split("|")[1:-1]]
        if len(parts) < 4:
            continue
        country, m, f, lang = parts[0], parts[1], parts[2], parts[3]
        out.append(f"η {country} · ο {m} · η {f} · τα {lang}")
    return out


