def strip_lead_dash(s: str) -> str:
    s = s.strip()
    while True:
        if s.startswith("- "):
            s = s[2:].strip()
        elif s.startswith("\u2013 "):
            s = s[2:].strip()
        else:
            break
    return s


def dialogue_tail(line: str) -> str:
    line = line.strip()
    if line.startswith("**") and ":**" in line:
        return line.split(":**", 1)[1].strip()
    return strip_lead_dash(line)


def strip_outer_md_bold(s: str) -> str:
    s = s.strip()
    if s.startswith("**") and s.endswith("**") and len(s) > 4:
        return s[2:-2].strip()
    return s


