def dialogue_tail(line: str) -> str:
    line = line.strip()
    if line.startswith("**") and ":**" in line:
        line = line.split(":**", 1)[1].strip()
    line = re.sub(r"\\?\_+", "…", line).strip()
    return line


def demd(s: str) -> str:
    s = s.strip()
    s = re.sub(r"\*+", "", s)
    return s.strip()


def unwrap_outer_italic(s: str) -> str:
    s = s.strip()
    if len(s) >= 2 and s[0] == "*" and s[-1] == "*" and not s.startswith("**"):
        return s[1:-1].strip()
    return s


def strip_lead_dash_logo(line: str) -> str:
    """`- **ΚΕΠ** — …` → без ведущего тире для чек-листа."""
    s = line.strip()
    if s.startswith("- "):
        return s[2:].strip()
    return s


def md_table_first_col(line: str) -> str:
    """Первая ячейка строки markdown-таблицы (| a | b |)."""
    s = line.strip()
    if not s.startswith("|"):
        return ""
    if ":---" in s or re.match(r"^\|\s*:?---", s):
        return ""
    parts = [p.strip() for p in s.split("|")]
    if len(parts) < 3:
        return ""
    c1 = parts[1]
    if not c1 or c1 in ("Δικαιολογητικό", "|", "Σειρά"):
        return ""
    return c1.replace("**", "").replace("✓", "").strip()


