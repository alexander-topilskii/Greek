"""Assemble book/pages/lesson_3/lesson_voice_3/voice_lesson_3.md from lesson_3_digitized.md slices."""

from __future__ import annotations

from pathlib import Path


def main() -> None:
    repo = Path(__file__).resolve().parents[1]
    digit_path = repo / "book/pages/lesson_3/lesson_digitized/lesson_3_digitized.md"
    out_path = repo / "book/pages/lesson_3/lesson_voice_3/voice_lesson_3.md"
    out_path.parent.mkdir(parents=True, exist_ok=True)

    lines = digit_path.read_text(encoding="utf-8").splitlines()

    def sl(a: int, b: int) -> str:
        return "\n".join(lines[a:b]).rstrip()

    mic = chr(0x1F399)
    house = chr(0x1F3E0)
    book = chr(0x1F4DA)
    page = chr(0x1F4C4)
    zap = chr(0x26A1)
    gem = chr(0x1F48E)

    a21 = sl(64, 75)
    pos_lene_family = sl(78, 88)
    rho_tao = sl(103, 110)
    gia_des = sl(119, 145)
    family_status = sl(148, 158)
    a22 = sl(263, 273)
    pos_lene_work = sl(276, 285)
    work_prompts = sl(302, 308)
    prof_table = sl(319, 333)
    eimai_pairs = sl(336, 355)
    lexicon = sl(166, 173)
    written_samples = sl(210, 215)
    ex9_sample = sl(382, 389)
    airplane = sl(571, 581)

    out: list[str] = []

    def ln(s: str = "") -> None:
        out.append(s)

    ln(f"# {mic} Голосовой урок 3 (по оцифровке главы)")
    ln()
    ln(
        f"**[{house} Readme](../../../../Readme.md) → [{book} content_3.md](../content_3.md) "
        f"→ {page} `lesson_voice_3/voice_lesson_3.md`**"
    )
    ln()
    ln(f"| {zap} Быстрые ссылки |                                                                    |")
    ln("|------------------|--------------------------------------------------------------------|")
    ln(f"| {book} Урок          | [content_3.md](../content_3.md)                                    |")
    ln(f"| {page} Оцифровка     | [lesson_3_digitized.md](../lesson_digitized/lesson_3_digitized.md) |")
    ln(f"| {gem} Суть урока    | [essence_3.md](../essence_3.md)                                    |")
    ln(f"| {mic} Voice (HTML)  | [essence_3.html](../essence_3.html)                                |")
    ln()

    ln("### Блок 1: Коммуникативные ситуации и ролевые игры")
    ln()

    ln("#### Ситуация 1 — У кафенείου: семейное фото")
    ln()
    ln("*Контекст:* Диалог **A21** и таблица **Πώς το λένε;** сразу после него.")
    ln()
    ln("```text")
    ln(a21)
    ln()
    ln(pos_lene_family)
    ln("```")
    ln()
    ln("*Роль А (линия Παναγιώτης):* Ведите вопросы о фото, родстве и именах — по репликам из первой части цитаты.")
    ln()
    ln("*Роль Б (вторая линия в A21):* Отвечайте по второй части цитаты; используйте формы **Τον λένε …**, **Τη λένε …**, представление родственников.")
    ln()
    ln("*Чек-фразы:* см. таблицу «Ερώτηση | Απάντηση» в цитате и сам диалог.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 2 — Кто это? Как зовут? Семейное положение")
    ln()
    ln("*Контекст:* Блок **Για δες** (вопросы «ποιος / ποια / ποιο» и «πώς … λένε») и зелёный блок **οικογενειακή κατάσταση**.")
    ln()
    ln("```text")
    ln(gia_des)
    ln()
    ln(family_status)
    ln("```")
    ln()
    ln("*Роль А:* Задавайте вопросы из оранжевых и зелёных шаблонов; переходите к **Είσαι παντρεμένος;** и ответу с **ελεύθερος**.")
    ln()
    ln("*Роль Б:* Отвечайте короткими репликами из цитаты (имена, род, статус).")
    ln()
    ln("*Чек-фразы:* списки вопросов и ответов внутри цитаты; не добавляйте новых слов.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 3 — После занятий: работа и семья")
    ln()
    ln("*Контекст:* Диалог **A22** и «κολλητάκια» **Πώς το λένε;** после него.")
    ln()
    ln("```text")
    ln(a22)
    ln()
    ln(pos_lene_work)
    ln("```")
    ln()
    ln("*Роль А (первая линия в A22):* Знакомство, **Τι κάνεις στην Ελλάδα;**, **Τι δουλειά κάνεις;**, вопросы о браке и совместной жизни — по тексту.")
    ln()
    ln("*Роль Б (вторая линия в A22):* Ответы с **δουλεύω**, **Είμαι γιατρός** / **ηλεκτρολόγος**, последняя реплика про супруга — дословно из цитаты.")
    ln()
    ln("*Чек-фразы:* цветные строки в таблице после диалога и все реплики A22.")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 4 — Мини-презентация: имя, профессия, место работы")
    ln()
    ln("*Контекст:* Образец из упражнения **9** (пункт 1) и фразы из **7 Απαντάω**.")
    ln()
    ln("```text")
    ln(work_prompts)
    ln()
    ln(ex9_sample)
    ln("```")
    ln()
    ln("*Роль А:* Задайте **Δουλεύεις;** и **Τι δουλειά κάνεις;** (из второй цитаты).")
    ln()
    ln("*Роль Б:* Ответьте одной-двумя фразами по образцу **Τη λένε … Είναι … και δουλεύει στο …**.")
    ln()
    ln("*Чек-фразы:* ручной образец в цитате; замените только имя, профессию и место из оцифровки (другие пункты **9**).")
    ln()
    ln("---")
    ln()

    ln("#### Ситуация 5 — В самолёте: карточки героев")
    ln()
    ln("*Контекст:* Задание **1.5** — герои в самолёте (карточки в оцифровке); опирайтесь только на оранжевый и синий блоки.")
    ln()
    ln("```text")
    ln(airplane)
    ln("```")
    ln()
    ln("*Роль А:* Один из героев по выбору; начните знакомство (имя, откуда, чем занимаетесь, семейное положение, где живёте) — только лексика из карточки.")
    ln()
    ln("*Роль Б:* Второй герой; отвечайте и задавайте встречные вопросы в той же лексике.")
    ln()
    ln("*Чек-фразы:* **οδοντίατρος**, **φοιτήτρια**, **παντρεμένος**, **ελεύθερη**, **μένει στο …**, **σπουδάζει …** — как в карточках.")
    ln()

    ln("### Блок 2: Шпаргалка для диалогов (фразы-клише)")
    ln()
    ln("*Срезы из оцифровки: семья и имена, вопросы к собеседнику, работа и брак.*")
    ln()
    ln("```text")
    ln(pos_lene_family)
    ln()
    ln(rho_tao)
    ln()
    ln(pos_lene_work)
    ln()
    ln(work_prompts)
    ln("```")
    ln()

    ln("### Блок 3: Тематический словарь (таблицы)")
    ln()
    ln("*Ниже — фрагменты оцифровки: лексикон семьи, образцы **Πώς τον/την λένε;**, три колонки профессий, списки **Είμαι …***")
    ln()
    ln("#### Семья (λέξεις из урока)")
    ln()
    ln("```text")
    ln(lexicon)
    ln("```")
    ln()
    ln("#### Образцы: ποιος / ποια, πώς λένε")
    ln()
    ln("```text")
    ln(written_samples)
    ln("```")
    ln()
    ln("#### Επαγγέλματα — τρεις στήλες")
    ln()
    ln("```text")
    ln(prof_table)
    ln("```")
    ln()
    ln("#### Είμαι … (ζεύγη και κοινό γένος)")
    ln()
    ln("```text")
    ln(eimai_pairs)
    ln("```")

    out_path.write_text("\n".join(out) + "\n", encoding="utf-8")


if __name__ == "__main__":
    main()
