def strip_lead_dash(s: str) -> str:
    s = s.strip()
    while True:
        if s.startswith("- "):
            s = s[2:].strip()
        elif s.startswith("\u2013 "):
            s = s[2:].strip()
        else:
            break
    return s


def dialogue_tail(line: str) -> str:
    line = line.strip()
    if line.startswith("**") and ":**" in line:
        return line.split(":**", 1)[1].strip()
    return strip_lead_dash(line)


def tidy_blanks(s: str) -> str:
    # В оцифровке пропуски часто как «\\_» (экранированный подчёркивание в Markdown).
    s = re.sub(r"(?:\\_){8,}", " … ", s)
    s = re.sub(r"_{5,}", " … ", s)
    s = re.sub(r"\s+", " ", s).strip()
    return s


def strip_trailing_book_note(s: str) -> str:
    """Убирает хвост вида « *(…пояснение из учебника…)» — внутри могут быть «)»."""
    s = s.strip()
    i = s.rfind(" *(")
    if i != -1:
        s = s[:i].rstrip()
    return s


def dt_clean(line: str) -> str:
    return tidy_blanks(dialogue_tail(line))


def bq(line: str) -> str:
    line = line.strip()
    if line.startswith(">"):
        return line[1:].strip()
    return line


def join_msg(L: list[str], a: int, b: int, line_shift: int) -> str:
    parts: list[str] = []
    for i in range(a, b + 1):
        t = L[i - line_shift].strip()
        if t.startswith(">"):
            parts.append(t[1:].strip())
    return " ".join(parts)


def story_cloud_sentence(s: str) -> str:
    s = s.strip()
    s = re.sub(r"^\d+\.\s*", "", s)
    s = re.sub(r"`\[[^\]]*\]\`\s*", "", s)
    return s.strip()


