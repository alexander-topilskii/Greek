def strip_lead_dash(s: str) -> str:
    s = s.strip()
    while True:
        if s.startswith("- "):
            s = s[2:].strip()
        elif s.startswith("\u2013 "):
            s = s[2:].strip()
        else:
            break
    return s


def dialogue_tail(line: str) -> str:
    line = line.strip()
    if line.startswith("**") and ":**" in line:
        return line.split(":**", 1)[1].strip()
    return strip_lead_dash(line)


def strip_trailing_book_note(s: str) -> str:
    s = s.strip()
    i = s.rfind(" *(")
    if i != -1:
        s = s[:i].rstrip()
    return s


def strip_blockquote(s: str) -> str:
    s = s.strip()
    if s.startswith("> "):
        return s[2:].strip()
    return s


def demd(s: str) -> str:
    s = s.strip()
    s = re.sub(r"\*+", "", s)
    return s.strip()


